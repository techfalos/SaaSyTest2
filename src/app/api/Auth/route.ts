import { cookies } from 'next/headers';
import { NextResponse } from 'next/server';

import { db_init, db_select, db_delete, db_update, db_add } from '../../../database/supabase';
const db = await db_init();

type OAuthProvider = 'google' | 'facebook' | 'apple'  | 'github';

const OAUTH_ENDPOINTS = {
  google: 'https://oauth2.googleapis.com/token',
  facebook: 'https://graph.facebook.com/v12.0/oauth/access_token',
  apple: 'https://appleid.apple.com/auth/token',
  github: 'https://github.com/login/oauth/access_token',
};

const USER_INFO_ENDPOINTS = {
  google: 'https://www.googleapis.com/oauth2/v2/userinfo',
  facebook: 'https://graph.facebook.com/me',
  apple: 'https://appleid.apple.com/auth/userinfo',
  github: 'https://api.github.com/user',
};

export async function POST(request: Request) {
  try {
    const { code, provider } = await request.json();
    
    if (!code || !provider) {
      return NextResponse.json(
        { error: 'No authorization code or provider specified' },
        { status: 400 }
      );
    }

    // Check if provider is valid OAuth provider (excluding email)
    const validProvider = provider as Exclude<OAuthProvider, 'email'>;
    if (!OAUTH_ENDPOINTS[validProvider]) {
      return NextResponse.json(
        { error: 'Invalid OAuth provider' },
        { status: 400 }
      );
    }

    let userData = {
        ID: '',
        name: '',
        avatar_url: '',
    };
    let tokenData;
   
    // Handle OAuth exchange manually
    const tokenResponse = await fetch(OAUTH_ENDPOINTS[validProvider], {
    method: 'POST',
    headers: {
        'Content-Type': 'application/x-www-form-urlencoded',
        Accept: 'application/json',
    },
    body: new URLSearchParams({
        client_id: process.env[`NEXT_PUBLIC_${provider.toUpperCase()}_CLIENT_ID`]!,
        client_secret: process.env[`${provider.toUpperCase()}_CLIENT_SECRET`]!,
        code,
        redirect_uri: `${process.env.NODE_ENV === 'production' ? process.env.DOMAIN : process.env.LOCAL_DOMAIN}/logincallback`,
        grant_type: 'authorization_code'
    }).toString(),
    });

    if (!tokenResponse.ok) {
        return NextResponse.json(
            { error: 'Failed to exchange code for token' },
            { status: 400 }
        );
    }
  
    tokenData = await tokenResponse.json();

    // Get user data from provider
    const userResponse = await fetch(USER_INFO_ENDPOINTS[validProvider], {
    headers: {
        Authorization: `Bearer ${tokenData.access_token}`,
    },
    });

    if (!userResponse.ok) {


    return NextResponse.json(
        { error: 'Failed to fetch user data' },
        { status: 400 }
    );
    }

    let fetchedUserData = await userResponse.json();

    switch (validProvider) {
   
        case 'google':
            userData.ID = fetchedUserData.id;
            userData.name = fetchedUserData.name;
            userData.avatar_url = fetchedUserData.picture;
            break;

        case 'facebook':
            userData.ID = fetchedUserData.id;
            userData.name = fetchedUserData.name;
            userData.avatar_url = fetchedUserData.picture?.data?.url;
            break;

        case 'apple':
            userData.ID = fetchedUserData.sub;
            userData.name = `${fetchedUserData.given_name || ''} ${fetchedUserData.family_name || ''}`.trim();
            // Apple doesn't provide avatar URL
            break;

        case 'github':
            userData.ID = fetchedUserData.id;
            userData.name = fetchedUserData.name;
            userData.avatar_url = fetchedUserData.avatar_url;
            break;

        default:
            return NextResponse.json(
            { error: 'Invalid provider' },
            { status: 400 }
            );
    }
    
    // Check if user exists
    const existingUser = await db_select(db, "User", ["*"], [], [{
      field: "OAuthID",
      op: "eq",
      value: userData.ID
    }]);

    // Generate UUID for new user
    let userId = crypto.randomUUID();
    
    if (existingUser.length === 0) {
      // Create new user
      // Check if this is the first user in the database
      const allUsers = await db_select(db, "User", ["ID"]);
      const isFirstUser = allUsers.length === 0;
      
      await db_add(db, 'User', {
        ID: userId,
        OAuthID: userData.ID,
        UserName: userData.name,
        AvatarURL: userData.avatar_url || '',
        UserLevel: '',
        IsAdmin: isFirstUser // Make the first user an admin
      });
    } else {
        userId = existingUser[0].ID;

        // Delete existing sessions
        const existingSessions = await db_select(db, 'UserSession', ['ID'], [], [{
            field: "UserID",
            op: "eq",
            value: userId
        }]);
        
        if (existingSessions && existingSessions.length > 0) {
            const sessionIds = existingSessions.map(session => session.ID);
            await db_delete(db, 'UserSession', sessionIds);
        }

      // Update existing user
      await db_update(db, 'User', {
        AvatarURL: userData.avatar_url || ''
      }, [userId]);
    }

    // Generate session ID
    const sessionId = crypto.randomUUID();
 

    // Create new session
    await db_add(db, 'UserSession', {
      ID: sessionId,
      SessionToken: tokenData.access_token,
      UserID: userId,
      ExpirationDate: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000) // 30 days
    });

    // Set session cookie
    const sessionCookie = await cookies();
    sessionCookie.set({
      name: 'session_id', 
      value: tokenData.access_token,
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: process.env.NODE_ENV === 'production' ? 'strict' : 'lax',
      maxAge: 30 * 24 * 60 * 60, // 30 days
      path: '/',
    });

    return NextResponse.json({ success: true });

  } catch (error) {
    return NextResponse.json(
      { error: error instanceof Error ? error.message : 'Authentication failed' },
      { status: 500 }
    );
  }
}

export async function GET() {
  try {
    // Get session ID from cookies
    const sessionCookie = await cookies();
    const sessionId = sessionCookie.get('session_id')?.value;

    if (!sessionId) {
      return NextResponse.json(
        { error: 'No session found' },
        { status: 401 }
      );
    }

    // Get session details
    const sessions = await db_select(db, 'UserSession', ["UserID"], [], [{
      field: 'SessionToken',
      op: 'eq', 
      value: sessionId
    }]);

    if (!sessions || sessions.length === 0) {
      return NextResponse.json(
        { error: 'Invalid session' },
        { status: 401 }
      );
    }

    // Get user details
    const users = await db_select(db, 'User', ["*"], [sessions[0].UserID]);

    if (!users || users.length === 0) {
      return NextResponse.json(
        { error: 'User not found' },
        { status: 404 }
      );
    }

    return NextResponse.json(users[0]);

  } catch (error) {
    return NextResponse.json(
      { error: error instanceof Error ? error.message : 'Failed to get user profile' },
      { status: 500 }
    );
  }
}

export async function DELETE() {
  try {
    // Get session ID from cookies
    const sessionCookie = await cookies();
    const sessionId = sessionCookie.get('session_id')?.value;

    if (!sessionId) {
      return NextResponse.json(
        { error: 'No session found' },
        { status: 401 }
      );
    }

    // Get session first
    const db = await db_init();
    const sessions = await db_select(db, 'UserSession', ['ID'], [], [{
      field: 'SessionToken',
      op: 'eq',
      value: sessionId
    }]);

    if (sessions && sessions.length > 0) {
      // Delete session from database
      await db_delete(db, 'UserSession', [sessions[0].ID]);
    }

    // Create response with cookie deletion
    const response = NextResponse.json({ success: true });
    response.cookies.delete('session_id');

    return response;

  } catch (error) {
    return NextResponse.json(
      { error: error instanceof Error ? error.message : 'Failed to logout' },
      { status: 500 }
    );
  }
}
