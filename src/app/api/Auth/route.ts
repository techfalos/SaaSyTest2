import { cookies } from 'next/headers';
import { NextResponse } from 'next/server';

import { db_init, db_query } from '../../../database/sqlite';
import { upsertUser } from '../../../database/users';

type OAuthProvider = 'google' | 'facebook' | 'apple'  | 'github';

const OAUTH_ENDPOINTS = {
  google: 'https://oauth2.googleapis.com/token',
  facebook: 'https://graph.facebook.com/v12.0/oauth/access_token',
  apple: 'https://appleid.apple.com/auth/token',
  github: 'https://github.com/login/oauth/access_token',
};

const USER_INFO_ENDPOINTS = {
  google: 'https://www.googleapis.com/oauth2/v2/userinfo',
  facebook: 'https://graph.facebook.com/me',
  apple: 'https://appleid.apple.com/auth/userinfo',
  github: 'https://api.github.com/user',
};

export async function POST(request: Request) {
  console.log('🔵 AUTH POST: Starting authentication flow');
  
  const db = await db_init();
  console.log('🔵 AUTH POST: Database initialized');
  
  try {
    console.log('🔵 AUTH POST: Parsing request body');
    const { code, provider } = await request.json();
    console.log('🔵 AUTH POST: Request parsed, provider:', provider);
    
    if (!code || !provider) {
      console.log('🔴 AUTH POST: Missing code or provider');
      return NextResponse.json(
        { error: 'No authorization code or provider specified' },
        { status: 400 }
      );
    }

    // Check if provider is valid OAuth provider (excluding email)
    const validProvider = provider as Exclude<OAuthProvider, 'email'>;
    if (!OAUTH_ENDPOINTS[validProvider]) {
      console.log('🔴 AUTH POST: Invalid provider:', validProvider);
      return NextResponse.json(
        { error: 'Invalid OAuth provider' },
        { status: 400 }
      );
    }

    console.log('🔵 AUTH POST: Provider validated, starting OAuth exchange');
    
    let userData = {
        ID: '',
        name: '',
        email: '',
        avatar_url: '',
    };
    let tokenData;
   
    // Handle OAuth exchange manually
    console.log('🔵 AUTH POST: Fetching token from OAuth provider');
    const tokenResponse = await fetch(OAUTH_ENDPOINTS[validProvider], {
        method: 'POST',
        headers: {
            'Content-Type': 'application/x-www-form-urlencoded',
            Accept: 'application/json',
        },
        body: new URLSearchParams({
            client_id: process.env[`NEXT_PUBLIC_${provider.toUpperCase()}_CLIENT_ID`]!,
            client_secret: process.env[`${provider.toUpperCase()}_CLIENT_SECRET`]!,
            code,
            redirect_uri: `${process.env.NODE_ENV === 'production' ? process.env.DOMAIN : process.env.LOCAL_DOMAIN}/logincallback`,
            grant_type: 'authorization_code'
        }).toString(),
    });

    console.log('🔵 AUTH POST: Token response received, status:', tokenResponse.status);

    if (!tokenResponse.ok) {
        console.log('🔴 AUTH POST: Token exchange failed');
        return NextResponse.json(
            { error: 'Failed to exchange code for token' },
            { status: 400 }
        );
    }
  
    tokenData = await tokenResponse.json();
    console.log('🔵 AUTH POST: Token data parsed successfully');

    // Get user data from provider
    console.log('🔵 AUTH POST: Fetching user data from provider');
    const userResponse = await fetch(USER_INFO_ENDPOINTS[validProvider], {
        headers: {
            Authorization: `Bearer ${tokenData.access_token}`,
        },
    });

    console.log('🔵 AUTH POST: User data response received, status:', userResponse.status);

    if (!userResponse.ok) {
      console.log('🔴 AUTH POST: Failed to fetch user data');
      return NextResponse.json(
        { error: 'Failed to fetch user data' },
        { status: 400 }
      );
    }

    let fetchedUserData = await userResponse.json();
    console.log('🔵 AUTH POST: User data parsed, processing by provider type');

    switch (validProvider) {
   
        case 'google':
            userData.ID = fetchedUserData.id;
            userData.name = fetchedUserData.name;
            userData.email = fetchedUserData.email;
            userData.avatar_url = fetchedUserData.picture;
            break;

        case 'facebook':
            userData.ID = fetchedUserData.id;
            userData.name = fetchedUserData.name;
            userData.email = fetchedUserData.email;
            userData.avatar_url = fetchedUserData.picture?.data?.url;
            break;

        case 'apple':
            userData.ID = fetchedUserData.sub;
            userData.name = `${fetchedUserData.given_name || ''} ${fetchedUserData.family_name || ''}`.trim();
            userData.email = fetchedUserData.email;
            // Apple doesn't provide avatar URL
            break;

        case 'github':
            userData.ID = fetchedUserData.id?.toString();
            userData.name = fetchedUserData.name || fetchedUserData.login;
            userData.email = fetchedUserData.email;
            userData.avatar_url = fetchedUserData.avatar_url;
            break;

        default:
            console.log('🔴 AUTH POST: Invalid provider in switch');
            return NextResponse.json(
            { error: 'Invalid provider' },
            { status: 400 }
            );
    }
    
    console.log('🔵 AUTH POST: User data processed, creating/updating user');
    // Create or update user using the new user management system
    const user = await upsertUser(
      userData.ID,
      validProvider,
      userData.name,
      userData.email,
      userData.avatar_url
    );
    
    
    // Delete existing sessions for this user
    console.log('🔵 AUTH POST: Checking for existing sessions');
    const existingSessions = await db_query(db, 
      "SELECT ID FROM UserSession WHERE userid = ?", 
      [user.userid]
    );
    
    console.log('🔵 AUTH POST: Found', existingSessions?.length || 0, 'existing sessions');
    
    if (existingSessions && existingSessions.length > 0) {
        const sessionIds = existingSessions.map(session => session.ID);
        const placeholders = sessionIds.map(() => '?').join(',');
        console.log('🔵 AUTH POST: Deleting existing sessions');
        await db_query(db, `DELETE FROM UserSession WHERE ID IN (${placeholders})`, sessionIds);
        console.log('🔵 AUTH POST: Existing sessions deleted');
    }

    // Generate session ID
    const sessionId = crypto.randomUUID();
    console.log('🔵 AUTH POST: Generated new session ID');

    // Create new session
    console.log('🔵 AUTH POST: Creating new session in database');
    await db_query(db, 
      "INSERT INTO UserSession (ID, SessionToken, userid, ExpirationDate) VALUES (?, ?, ?, ?)",
      [sessionId, tokenData.access_token, user.userid, new Date(Date.now() + 30 * 24 * 60 * 60 * 1000).toISOString()]
    );
    console.log('🔵 AUTH POST: New session created in database');

    // Set session cookie
    console.log('🔵 AUTH POST: Setting session cookie');
    const sessionCookie = await cookies();
    sessionCookie.set({
      name: 'session_id', 
      value: tokenData.access_token,
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: process.env.NODE_ENV === 'production' ? 'strict' : 'lax',
      maxAge: 30 * 24 * 60 * 60, // 30 days
      path: '/',
    });
    console.log('🔵 AUTH POST: Cookie set, preparing response');

    console.log('🔵 AUTH POST: Creating JSON response');
    
    // Create a completely clean object to avoid any database result object issues
    const cleanUserData = {
      userid: String(user.userid),
      userName: String(user.UserName),
      avatarURL: String(user.AvatarURL || ''),
      userLevel: Number(user.UserLevel),
      isAdmin: Number(user.UserLevel) === 2
    };
    
    return NextResponse.json({ 
      success: true,
      user: cleanUserData
    });

  } catch (error) {
    console.error('🔴 AUTH POST: Error occurred:', error);
    return NextResponse.json(
      { error: error instanceof Error ? error.message : 'Authentication failed' },
      { status: 500 }
    );
  }
}

