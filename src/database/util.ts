/**
 * Sanitizes a table name by replacing non-alphanumeric characters with underscores
 * and converting to lowercase
 * @param tableName The table name to sanitize
 * @returns The sanitized table name
 */
export function sanitizeTableName(tableName: string): string {
  return tableName.replace(/[^A-Za-z0-9]/g, '_');
}
